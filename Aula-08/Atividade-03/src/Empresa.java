public class Empresa {
  private String nome;
  private Software software;

  public void setNome(String nome) {
    this.nome = nome;
  }

  public void setSoftware(Software software) {
    this.software = software;
  }

  public String getNome() {
    return nome;
  }

  public Software getSoftware() {
    return software;
  }
}
