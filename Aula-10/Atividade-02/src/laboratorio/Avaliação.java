public class Avaliação {
  private double nota;
  private Professor professor;

  public void setNota(double nota) {
    this.nota = nota;
  }

  public void setProfessor(Professor professor) {
    this.professor = professor;
  }

  public double getNota() {
    return nota;
  }

  public Professor getProfessor() {
    return professor;
  }
}
