
public class Maritmo extends Transporte{
	private String nome;
	private int calado;
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public void setCalado(int calado) {
		this.calado = calado;
	}
	
	public String getNome() {
		return nome;
	}
	
	public int getCalado() {
		return calado;
	}
}
